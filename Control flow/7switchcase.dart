void main() {
  //switch case statement

  String grade = "B";

  switch (grade) {
    case 'A':
      print(" Excellent grade of A");
      break;
    case 'B':
      print("Very good ");
      break;
    case 'C':
      print("Good enough but work hard");
      break;
    case 'F':
      print("You are failed");
      break;
    default:
      print("Invalid");
  }

  //String and int allowed
  // Double and Boolean Not allowed
}
